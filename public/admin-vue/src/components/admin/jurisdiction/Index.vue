                return statusMap[parseInt(status)]
            },
            statusShow(status){
                const statusMap = {
                    0:'正常',
                    1:'禁用'
                };
                return statusMap[parseInt(status)]
            },
            positionShow(status){
                const statusMap = {
                    1:'主页',
                    2:'新闻'
                };
                return statusMap[parseInt(status)]
            },
            labelShow(label){
                const labelMap = {
                    0:'普通',
                    1:'火爆',
                    2:'最新',
                    3:'推荐',
                };
                return labelMap[parseInt(label)]
            }
        
        },
}
