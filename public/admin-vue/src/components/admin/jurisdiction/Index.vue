export default {
  data() {
    return {
            GameData: [],
            ImgPrefix:process.env.IMAGE_PREFIX,
            defaultMsg: '这里是UE测试',
            loading:false,
            config: {
              initialFrameWidth: null,
              initialFrameHeight: 350
            },
